/*	Conditional compilation flags					*/
/*									*/
/*	Some of these may be commented out to set them to "off".	*/
/*	The comment should be removed to set them to "on".		*/
/*									*/
/*	Form:	xXTM  = timing information (not supported)		*/
/*		xXTR1 = trace level 1					*/
/*		xXTR2 = trace level 2, implies level 1			*/
/*		xXTR3 = trace level 3, implies level 1 and 2		*/
/*	where	 X     = tracing object					*/
/*									*/

/* gutil, iutil and access methods compilation flags */
# define	xATR1
# define	xATR2
# define	xATR3

/* decomposition compilation flags */
# define	xDTR1

/* LIBQ.A compilation flags */
# define	xETR1
# define	xETR2
# define	xETR3

/* monitor compilation flags */
# define	xMTR1
# define	xMTR2
# define	xMTR3
/* # define	xMTRM */	/* macro processor */

/* OVQP compilatiion flags */
# define	xOTR1

/* parser compilation flags */
# define	xPTR1
# define	xPTR2
# define	xPTR3

/* qrymod compilation flags */
# define	xQTR1
# define	xQTR2
# define	xQTR3

/* support and sys compilation flags */
# define	xSTR1
# define	xSTR2
# define	xSTR3

/* DBU compilation flags */
# define	xZTR1
# define	xZTR2
# define	xZTR3
